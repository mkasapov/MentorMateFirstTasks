package reflectionTask;

public class Example {
    private String exampleName;
    private int age;
    private String exampleExample;

    public Example() {
    }

    public Example(String exampleName, int age, String exampleExample) {
        this.exampleName = exampleName;
        this.age = age;
        this.exampleExample = exampleExample;
    }

    public String getExampleName() {
        return exampleName;
    }

    public void setExampleName(String exampleName) {
        this.exampleName = exampleName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getExampleExample() {
        return exampleExample;
    }

    public void setExampleExample(String exampleExample) {
        this.exampleExample = exampleExample;
    }
}
